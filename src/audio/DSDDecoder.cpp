DSDDecoder::DSDDecoder() = default;

DSDDecoder::~DSDDecoder() {
    close();
}

bool DSDDecoder::open(const std::string& filepath) {
    ouroboros::util::Logger::debug("DSDDecoder: Opening file: " + filepath);

    file_ = std::fopen(filepath.c_str(), "rb");
    if (!file_) {
        ouroboros::util::Logger::error("DSDDecoder: Failed to open file: " + filepath);
        return false;
    }

    if (!parse_dsf_header()) {
        std::fclose(file_);
        file_ = nullptr;
        return false;
    }

    // Select filter coefficients based on DSD rate
    const float* coeffs = nullptr;
    int decimation_ratio = static_cast<int>(dsd_sample_rate_ / OUTPUT_RATE);

    switch (decimation_ratio) {
        case 8:   coeffs = DSD64_COEFFS;  num_groups_ = DSD64_GROUPS;  break;
        case 16:  coeffs = DSD128_COEFFS; num_groups_ = DSD128_GROUPS; break;
        case 32:  coeffs = DSD256_COEFFS; num_groups_ = DSD256_GROUPS; break;
        case 64:  coeffs = DSD512_COEFFS; num_groups_ = DSD512_GROUPS; break;
        default:
            ouroboros::util::Logger::error("DSDDecoder: Unsupported DSD rate: " +
                std::to_string(dsd_sample_rate_) + " Hz (decimation ratio " +
                std::to_string(decimation_ratio) + ")");
            std::fclose(file_);
            file_ = nullptr;
            return false;
    }

    bytes_per_output_ = decimation_ratio / 8;
    total_output_frames_ = static_cast<long>(total_dsd_samples_ / decimation_ratio);
    output_position_ = 0;

    // Build byte lookup tables
    if (!build_lut(coeffs, num_groups_)) {
        std::fclose(file_);
        file_ = nullptr;
        return false;
    }

    // Initialize per-channel overlap buffers (zero-filled = silence on first block)
    overlap_.resize(channels_);
    for (auto& ov : overlap_) {
        ov.assign(num_groups_ - 1, 0);
    }

    // Compute output frames per block
    block_output_frames_ = static_cast<int>((static_cast<int64_t>(block_size_) * 8) / (bytes_per_output_ * 8));
    block_frames_consumed_ = block_output_frames_; // force first read

    // Allocate block buffer and per-channel data
    block_buffer_.resize(static_cast<size_t>(block_size_) * channels_);
    ch_data_.resize(channels_);
    for (auto& ch : ch_data_) {
        ch.resize(block_size_);
    }

    // Residual buffer
    residual_.resize(static_cast<size_t>(block_output_frames_) * channels_);
    residual_frames_ = 0;
    residual_offset_ = 0;

    int dsd_mult = static_cast<int>(dsd_sample_rate_ / 44100);
    ouroboros::util::Logger::info("DSDDecoder: Opened DSD" + std::to_string(dsd_mult) +
        " (" + std::to_string(dsd_sample_rate_) + " Hz) - " +
        std::to_string(channels_) + "ch, " +
        std::to_string(total_output_frames_) + " output frames at " +
        std::to_string(OUTPUT_RATE) + " Hz, filter=" +
        std::to_string(num_groups_) + " groups");

    return true;
}

void DSDDecoder::close() {
    if (file_) {
        std::fclose(file_);
        file_ = nullptr;
    }
    channels_ = 0;
    dsd_sample_rate_ = 0;
    total_dsd_samples_ = 0;
    block_size_ = 0;
    data_offset_ = 0;
    data_size_ = 0;
    bytes_per_output_ = 0;
    num_groups_ = 0;
    total_output_frames_ = 0;
    output_position_ = 0;
    block_output_frames_ = 0;
    block_frames_consumed_ = 0;
    lut_.clear();
    overlap_.clear();
    block_buffer_.clear();
    ch_data_.clear();
    residual_.clear();
    residual_frames_ = 0;
    residual_offset_ = 0;
}

